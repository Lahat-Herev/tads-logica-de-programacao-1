#include <stdio.h>
int main()
{
	int tamanhoArquivo, velocidadeConexao;
	float tempoDownload;
	printf("Entre com a velocidade contratada de internet (em bits): ");
	scanf("%d", &velocidadeConexao);
	printf("\n Entre com o tamanho do arquivo (em bytes): ");
	scanf("%d", &tamanhoArquivo);
	tempoDownload = (float) tamanhoArquivo / (velocidadeConexao / 8);
	printf("\n O tempo necessario para o download do arquivo em segundos e de: %.2f", tempoDownload);
	return 0;
}
